function suma(a, b){
    return a+b;
}

function resta(a, b){
    return a-b;
}

function multiplicacion(a, b){
    return a*b;
}

function divicion(a, b){
    return a/b;
}

console.log("Suma:",suma(4, 5));
console.log("Resta:",resta(3, 6));
console.log("Multiplicación:",multiplicacion(2,7));
console.log("División:",divicion(20,4));